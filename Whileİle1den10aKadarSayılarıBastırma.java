package örnekler;

public class Whileİle1den10aKadarSayılarıBastırma {

	public static void main(String[] args) {


		//1 den 10 a kadar sayıları while döngüsü ile bastırmak . 
		
		int number=1;
		while(number<=10)
		{
			System.out.print("number = " + number + "\t");
			number++;
		}

	}

}
