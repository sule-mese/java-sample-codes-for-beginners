
public class Escapes {
	public static void main(String[] args) {

		System.out.println("Backspace: " + '\b');
		System.out.println("Tab: =>" + '\t' + "<=");
		System.out.println("Linefeed: " + '\n');
		System.out.println("Form feed: " + '\f');
		System.out.println("Carriage return: " + '\r');
		System.out.println("Apostrophe-quote: " + '\'');
		System.out.println("Quotation mark: " + "\"Ali'nin\"\n");
		System.out.println("Backslash: " + '\\');
		
		System.out.println("boþluk býrakma : " + '\b');
		System.out.println(" tab  kadar boþluk býrakma : " + '\t');
		System.out.println("alt satýra atlama :" + '\n');
		System.out.println("ali \'nin mi o acaba " + '\t');

		
		
	}
	
}
